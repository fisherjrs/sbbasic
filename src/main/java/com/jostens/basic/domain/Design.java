package com.jostens.basic.domain;

public class Design {

	private Long designId;
	private String trimCode;
	private String bindingCode;
	private String pageCreation;

	public Long getDesignId() {
		return designId;
	}

	public void setDesignId(Long designId) {
		this.designId = designId;
	}

	public String getTrimCode() {
		return trimCode;
	}

	public void setTrimCode(String trimCode) {
		this.trimCode = trimCode;
	}

	public String getBindingCode() {
		return bindingCode;
	}

	public void setBindingCode(String bindingCode) {
		this.bindingCode = bindingCode;
	}

	public String getPageCreation() {
		return pageCreation;
	}

	public void setPageCreation(String pageCreation) {
		this.pageCreation = pageCreation;
	}

}
